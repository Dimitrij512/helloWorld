package com.dmytro.andrusiv.model;

public class PartOfDay {
	private String morning = "06:00:00";
	private String day = "09:00:00";
	private String evening = "19:00:00";
	private String night = "23:00:00";

	// geters
	public String getMorning() {
		return morning;
	}

	public String getDay() {
		return day;
	}

	public String getEvening() {
		return evening;
	}

	public String getNight() {
		return night;
	}

}
